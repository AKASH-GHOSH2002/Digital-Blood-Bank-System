const mongoose = require("mongoose");
const inventoryModel = require("../models/inventoryModel");
const userModel = require("../models/userModel");
const createInventoryController = async (req, res) => {
  try {
    const { email, inventoryType, bloodGroup, quantity, userId } = req.body;

    // 1. Validate user
    const user = await userModel.findOne({ email });
    if (!user) {
      return res.status(404).send({
        success: false,
        message: "User Not Found",
      });
    }

    // 2. Handle inventoryType = "in"
    if (inventoryType === "in") {
      if (user.role !== "donar") {
        return res.status(403).send({
          success: false,
          message: "Only donors can add blood (inventoryType: in)",
        });
      }
      req.body.donar = user._id;
    }

    // 3. Handle inventoryType = "out"
    else if (inventoryType === "out") {
      if (user.role !== "hospital") {
        return res.status(403).send({
          success: false,
          message: "Only hospitals can request blood (inventoryType: out)",
        });
      }

      const organisation = new mongoose.Types.ObjectId(userId);

      // Calculate available quantity
      const totalInResult = await inventoryModel.aggregate([
        {
          $match: {
            organisation,
            inventoryType: "in",
            bloodGroup,
          },
        },
        {
          $group: {
            _id: "$bloodGroup",
            total: { $sum: "$quantity" },
          },
        },
      ]);
      const totalIn = totalInResult[0]?.total || 0;

      const totalOutResult = await inventoryModel.aggregate([
        {
          $match: {
            organisation,
            inventoryType: "out",
            bloodGroup,
          },
        },
        {
          $group: {
            _id: "$bloodGroup",
            total: { $sum: "$quantity" },
          },
        },
      ]);
      const totalOut = totalOutResult[0]?.total || 0;

      const available = totalIn - totalOut;

      if (available < quantity) {
        return res.status(400).send({
          success: false,
          message: `Only ${available}ML of ${bloodGroup.toUpperCase()} is available`,
        });
      }

      req.body.hospital = user._id;
    }

    // 4. Invalid inventoryType
    else {
      return res.status(400).send({
        success: false,
        message: "Invalid inventory type",
      });
    }

    // 5. Save inventory record
    const inventory = new inventoryModel(req.body);
    await inventory.save();

    return res.status(201).send({
      success: true,
      message: "New blood record added successfully",
    });

  } catch (error) {
    console.log(error);
    return res.status(500).send({
      success: false,
      message: "Error in Create Inventory API",
      error,
    });
  }
};


